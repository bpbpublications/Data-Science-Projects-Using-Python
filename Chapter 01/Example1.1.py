# Numeric Types
age = 28                  # Integer (int)
temperature = -5.75       # Floating-point number (float)
# Sequence Types
city_name = "Delhi"    # String (str)
colors = ["red", "green", "blue"]  # List
# Mapping Type
book = {"title": "Python Basics", "pages": 320}  # Dictionary (dict)
# Set Types
rollnumbers = {4, 7, 15, 22}    # Set
regid = frozenset({101, 102})   # Frozenset
# Boolean Type
has_passed = False         # Boolean (bool)
# None Type
response = None            # NoneType (represents no value)
# Type Checking using type()
print(type(age))             # <class 'int'>
print(type(temperature))     # <class 'float'>
print(type(city_name))       # <class 'str'>
print(type(colors))          # <class 'list'>
print(type(book))            # <class 'dict'>
print(type(rollnumbers)) # <class 'set'>
print(type(regid))   # <class 'frozenset'>
print(type(has_passed))      # <class 'bool'>
print(type(response))        # <class 'NoneType'>
