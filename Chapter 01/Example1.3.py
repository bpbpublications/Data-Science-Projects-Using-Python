# Convert float to int
x = 2.8
y = int(x) #y will be 2(integer)
print(y)
# Convert int to float
a = 5
b = float(a)  #b will be 5.0(float)
print(b)
# Convert string to int
num_str = "100"
num = int(num_str)  # num will be 100(integer)
print(num)
# Convert int to string
num_str2 = str(num)  # num_str2 will be "100"(string)
print(num_str2)
