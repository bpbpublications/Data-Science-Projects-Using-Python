a = 10
b = 4
print(a+b)  # Output:14(addition)
print(a-b)  # Output: 6(subtraction)
print(a*b)  # Output: 40(multiplication)
print(a/b)  # Output: 2.5(division)
print(a%b)  # Output: 2(modulus i.e. remainder)
print(a**b) # Output: 10000(exponentiation i.e.power,10^4)
print(a//b) # Output: 2(floor division)
