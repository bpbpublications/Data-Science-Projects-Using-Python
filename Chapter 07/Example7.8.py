import numpy as np
# Create an array
var1 = np.array([12, 22, 23, 34, 45])
# Element-wise operations
square1 = np.square(var1)  # Squares each element
sqrt1 = np.sqrt(var1)   # Square root of each element
exp1 = np.exp(var1)     # Exponential of each element
print("Original array displayed:", var1)
print("Square of each element:", square1)
print("Square root of each element:", sqrt1)
print("Exponential of each element:", exp1)
